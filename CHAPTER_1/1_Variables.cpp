#include <iostream>
using namespace std;

int main() {
    cout<<"Hello Sanu "<<endl; //"<<endl" and "\n" is used to write code in next line
    cout<<"Hello Kumar\n";
    cout<<"Hello Sanu\n";
    cout<<"Hello Kumar\n";
    cout<<3*2<<endl;   // here (3*2) without inverted comma will write ans of this value 
    cout<<"3*2\n";      // writing ("3*2") in inverted comma will directly print the exact digit

    // Variables and Data Types

    int a = 10; // variable declare
    cout<<a<<endl; // here a is  int i.e integer data type 

    char c = 'f'; // here c is variable of character  data type
    cout<<c<<endl;       // in char data type only one character can be used at a time

    float f = 4.67;  // float data type is used  for decimals 
    cout<<f<<endl;

    double d = 6.64646464; // double data type is same as flout data type but used for more than 4 byte
    cout<<d<<endl;

    bool b = 1; // boolean data type is used for yes or no or true or false
    cout<<b<<endl;

    bool b1 = false; // boolean data type is used for yes or no or true or false
    cout<<b1<<endl;


    return 0;

}
